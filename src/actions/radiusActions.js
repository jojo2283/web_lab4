export const setNewRadius = (newRadius) => {
  return {
    type: 'SET_RADIUS',
    payload: newRadius,
  };
};
export const setxValue = (xValue) => {
  return {
    type: 'SET_X',
    payload: xValue,
  };
};
export const setyValue = (yValue) => {
  return {
    type: 'SET_Y',
    payload: yValue,
  };
};
